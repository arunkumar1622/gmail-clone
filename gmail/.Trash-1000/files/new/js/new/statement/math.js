//math.round
a = 4.4;
console.log(Math.round(a));//4

b = -98.5;
console.log(Math.round(b)); //98

//math.ceil
math_1 = 5.3;
console.log(Math.ceil(math_1)); //6

math_2 = -5.7;
console.log(Math.ceil(math_2));  //-5

//math.floor
math_3 = 55.3;
console.log(Math.floor(math_3)); // 55

math_4 = -55.8;
console.log(Math.floor(math_4)); // -56

//Math trunc
math_a = 22.2;
console.log(Math.trunc(math_a)); // 22

//math pow
console.log(Math.pow(6,2)); // 36

//math sqrt
console.log(Math.sqrt(55)); //7.416198487095663
console.log(Math.sqrt(36)); //6

